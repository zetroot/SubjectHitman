namespace SubjectHitman.Domain.Entities;

/// <summary>
/// Типы ключей поиска субъекта, производные от приложения 2 к указанию Банка России 5791-У
/// после объединения пар «текущие/предыдущие» наборы данных. Меньшее числовое значение = более сильный ключ.
/// </summary>
public enum SearchKeyType : short
{
    /// <summary>ФИО + код вида ДУЛ, серия и номер (наборы 5791-У 1.1 + 1.2).</summary>
    K1 = 1,

    /// <summary>Фамилия + дата рождения + код вида ДУЛ, серия и номер (наборы 1.3 + 1.4).</summary>
    K2 = 2,

    /// <summary>Серия, номер, дата выдачи ДУЛ + ИНН (наборы 1.5 + 1.6).</summary>
    K3 = 3,

    /// <summary>Серия, номер ДУЛ + СНИЛС (наборы 1.7 + 1.8).</summary>
    K4 = 4,

    /// <summary>Дата рождения + СНИЛС (набор 1.11).</summary>
    K5 = 5,

    /// <summary>Дата рождения + ИНН (набор 1.12).</summary>
    K6 = 6,
}
